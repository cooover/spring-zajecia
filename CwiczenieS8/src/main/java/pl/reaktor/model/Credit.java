package pl.reaktor.model;

public class Credit {

	private double k0;
	private int n;
	private double r=0.1;
	
	public double getK0() {
		return k0;
	}
	public void setK0(double k0) {
		this.k0 = k0;
	}
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}
	public double getR() {
		return r;
	}
	public void setR(double r) {
		this.r = r;
	}
	
}
